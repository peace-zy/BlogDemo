package xm.ruleengine.drools;

/**
 * @author xuming
 */
public class User {
    private int money;
    private int emptyBottle;
    private int totals;

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getEmptyBottle() {
        return emptyBottle;
    }

    public void setEmptyBottle(int emptyBottle) {
        this.emptyBottle = emptyBottle;
    }

    public int getTotals() {
        return totals;
    }

    public void setTotals(int totals) {
        this.totals = totals;
    }
}
